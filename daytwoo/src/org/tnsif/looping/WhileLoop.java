
		package org.tnsif.looping;
		import java.util.Scanner;
		public class WhileLoop
		{

			public static void main(String[] args) {
				Scanner sc=new Scanner(System.in);
					
				int n =sc.nextInt();
				int i=1;
				while (n>=i)
		       
			    {
			    	System.out.print(n+   "" );
			    	n--;
			    }
			       
	}

}
