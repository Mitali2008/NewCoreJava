package org.tnsif.looping;
		import java.util.Scanner;
		public class DoLoop
		{

			public static void main(String[] args) {
				Scanner sc=new Scanner(System.in);
					
				int n =sc.nextInt();
				do {
					
					System.out.println("VALUE OF N IS"  + n );
					n++;
					
				}while(n<14);
			
				    }
				}
			
				
				
	
